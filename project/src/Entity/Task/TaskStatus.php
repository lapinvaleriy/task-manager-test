<?php

namespace App\Entity\Task;

use Doctrine\ORM\Mapping as ORM;

/**
 * Class TaskStatus
 *
 * @ORM\Table(name="task_task_statuses")
 * @ORM\Entity(repositoryClass="App\Repository\Task\TaskStatusRepository")
 */
class TaskStatus
{
    /**
     * @var int
     *
     * @ORM\Column(name="id", type="integer")
     * @ORM\Id
     * @ORM\GeneratedValue(strategy="AUTO")
     */
    private int $id;

    /**
     * @var string
     *
     * @ORM\Column(name="name", type="string")
     */
    private string $name;

    /**
     * @return int
     */
    public function getId(): int
    {
        return $this->id;
    }

    /**
     * @return string
     */
    public function getName(): string
    {
        return $this->name;
    }

    /**
     * @param string $name
     */
    public function setName(string $name): void
    {
        $this->name = $name;
    }
}